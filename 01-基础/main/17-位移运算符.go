package main

import (
	"fmt"
)

/**
* 右移运算符(>>): 低位溢出, 符号位不变, 并用符号位补溢出的高位
* 左移运算符(<<): 符号位不变, 低位补零
 */
func main() {
	fmt.Println(1 >> 2) // 0000 0001(原码) => 0000 0001(反码) => 0000 0000 = 0
	fmt.Println(1 << 2) // 0000 0001(原码) => 0000 0001(反码) => 0000 0100 = 4

	// TODO 这里的运算逻辑不对
	fmt.Println(-1 >> 2) // 1000 0001(原码) => 1111 1110(反码) =>  1000 0001= -1
	fmt.Println(-1 << 2) // 1000 0001(原码) => 1111 1110(反码) =>  1000 0100= -4
}
